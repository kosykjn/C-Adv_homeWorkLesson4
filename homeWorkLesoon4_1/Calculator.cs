namespace homeWorkLesoon4_1
{
    static class Calculator
    {
        public static dynamic Add(int firstNumber, int secondNumber)
        {
            return firstNumber + secondNumber;
        }
        public static dynamic Sub(int firstNumber, int secondNumber)
        {
            return firstNumber - secondNumber;
        }
        public static dynamic Mult(int firstNumber, int secondNumber)
        {
            return firstNumber * secondNumber;
        }
        public static dynamic Div(int firstNumber, int secondNumber)
        {
            return firstNumber / secondNumber;
        }
    }
}
